# Python cto demonstrate the working of logarithm
 
import math
 
 
print ("The value of log 2 with base 3 is : ", end="")
print (math.log(2,3))
 
print ("The value of log2 of 16 is : ", end="")
print (math.log2(16))
    
print ("The value of log10 of 10000 is : ", end="")
print (math.log10(10000))
